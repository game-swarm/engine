//! SDK code generation from IDL.
//!
//! Generates TypeScript and Rust SDK code from an IdlDoc.
//! For TypeScript, custom actions are expanded as concrete action types
//! (方案β). For Rust, custom actions are generated as helper constructors
//! on CommandAction (the Rust equivalent of concrete types).

use crate::{
    command::{
        CommandSchemaBranch, CommandSchemaField, SPECIAL_COMMAND_ACTIONS, command_schema_branches,
    },
    idl::{EnumDefs, IdlDoc, ModCustomAction},
};
use swarm_engine_api::abi::ABI_VERSION;
use swarm_engine_plugin_sdk::components::BodyPartRegistry;

// ── TypeScript generation ───────────────────────────────────────────

pub fn generate_typescript(idl: &IdlDoc) -> String {
    let mut out = String::new();

    out.push_str(&ts_header(idl));
    out.push('\n');
    out.push_str(&ts_primitive_types());
    out.push('\n');
    out.push_str(&ts_enums(&idl.core.enums));
    out.push('\n');
    out.push_str(&ts_structure_types(idl));
    out.push('\n');
    out.push_str(&ts_core_actions(idl));
    out.push('\n');
    out.push_str(&ts_builtin_dispatch_actions());
    out.push('\n');
    out.push_str(&ts_custom_actions(&idl.mods.custom_actions));
    out.push('\n');
    out.push_str(&ts_action_union(idl));
    out.push('\n');
    out.push_str(&ts_command_factories(idl));
    out.push('\n');
    out.push_str(&ts_realtime_types());
    out.push('\n');
    out.push_str(&ts_constants(idl));
    out
}

fn ts_header(idl: &IdlDoc) -> String {
    format!(
        "// @generated by swarm-engine idl v{idl_version}\n\
         // world hash: {world_hash}\n\
         // engine version: {engine_version}\n\
         // DO NOT EDIT — this file is auto-generated.\n",
        idl_version = idl.idl_version,
        world_hash = idl.world_hash,
        engine_version = idl.engine_version,
    )
}

fn ts_primitive_types() -> String {
    r#"
export type UInt32 = number;
export type UInt64 = number | bigint;
export type PlayerId = UInt32;
export type RoomId = UInt32;
export type ObjectId = UInt64;
export type Tick = UInt64;
export type ResourceName = string;
export type ResourceAmount = UInt32;
export type ResourceCost = Record<ResourceName, ResourceAmount>;

export interface Position {
  x: number;
  y: number;
  room: RoomId;
}
"#
    .to_string()
}

fn ts_enums(enums: &EnumDefs) -> String {
    let mut out = String::new();

    // Direction
    out.push_str("export type Direction = \"North\" | \"South\" | \"East\" | \"West\";\n\n");

    // BodyPart
    out.push_str("export type BodyPart = ");
    for (i, v) in enums.body_part.iter().enumerate() {
        if i > 0 {
            out.push_str(" | ");
        }
        out.push_str(&format!("\"{v}\""));
    }
    out.push_str(";\n\n");

    // DamageType
    out.push_str("export type DamageType = ");
    for (i, v) in enums.damage_type.iter().enumerate() {
        if i > 0 {
            out.push_str(" | ");
        }
        out.push_str(&format!("\"{v}\""));
    }
    out.push_str(";\n\n");

    // RejectionReason
    out.push_str("export type RejectionReasonCode = ");
    for (i, v) in enums.rejection_reason.iter().enumerate() {
        if i % 8 == 0 && i > 0 {
            out.push_str("\n  | ");
        } else if i > 0 {
            out.push_str(" | ");
        }
        out.push_str(&format!("\"{v}\""));
    }
    out.push_str(";\n");

    out
}

fn ts_structure_types(idl: &IdlDoc) -> String {
    let mut out = String::from("export type StructureType = ");
    let all: Vec<&str> = idl
        .core
        .structure_types
        .iter()
        .chain(idl.mods.structure_types.iter().map(|s| &s.name))
        .map(|s| s.as_str())
        .collect();
    for (i, s) in all.iter().enumerate() {
        if i > 0 {
            out.push_str("\n  | ");
        }
        out.push_str(&format!("\"{s}\""));
    }
    out.push_str(";\n");
    out
}

fn ts_core_actions(idl: &IdlDoc) -> String {
    let mut out = String::new();

    for cmd in &idl.core.commands {
        if cmd.name == "Action" || SPECIAL_COMMAND_ACTIONS.contains(&cmd.name.as_str()) {
            continue;
        }
        out.push_str(&format!(
            "export interface {name}Action {{\n",
            name = cmd.name
        ));
        out.push_str(&format!("  type: \"{name}\";\n", name = cmd.name));
        for (field, ftype) in &cmd.fields {
            let ts_type = idl_to_ts_type(ftype);
            let optional = if ftype.ends_with('?') { "?" } else { "" };
            out.push_str(&format!("  {field}{optional}: {ts_type};\n"));
        }
        out.push_str("}\n\n");
    }

    out
}

fn ts_builtin_dispatch_actions() -> String {
    let mut out = String::from("// ── Built-in ActionRegistry wire actions ──\n\n");
    for name in SPECIAL_COMMAND_ACTIONS {
        out.push_str(&format!(
            "export interface {name}Action {{\n  type: \"{name}\";\n{fields}}}\n\n",
            fields = ts_special_action_fields(),
        ));
    }
    out
}

fn ts_special_action_fields() -> &'static str {
    "  object_id: ObjectId;\n  target_id: ObjectId;\n  resource?: ResourceName;\n  amount?: ResourceAmount;\n  range?: number;\n  structure?: StructureType;\n  damage_type?: DamageType;\n  cooldown?: number;\n"
}

fn idl_to_ts_type(idl_type: &str) -> String {
    match idl_type {
        "ObjectId" => "ObjectId".into(),
        "PlayerId" => "PlayerId".into(),
        "Direction" => "Direction".into(),
        "ResourceName" | "ResourceName?" => "ResourceName".into(),
        "ResourceAmount" => "ResourceAmount".into(),
        "u32" => "number".into(),
        "u64" => "UInt64".into(),
        "i32" => "number".into(),
        "StructureType" => "StructureType".into(),
        "BodyPart[]" => "BodyPart[]".into(),
        "String" => "string".into(),
        "JsonValue" => "unknown".into(),
        other => {
            if let Some(base) = other.strip_suffix('?') {
                idl_to_ts_type(base).to_string() // optional fields just omit the ?
            } else {
                format!("unknown /* {other} */")
            }
        }
    }
}

fn ts_custom_actions(actions: &[ModCustomAction]) -> String {
    if actions.is_empty() {
        return String::new();
    }

    let mut out = String::from("// ── Custom actions (from world.toml mods) ──\n\n");

    for action in actions {
        if SPECIAL_COMMAND_ACTIONS.contains(&action.name.as_str()) {
            continue;
        }
        out.push_str(&format!(
            "export interface {name}Action {{\n  type: \"{name}\";\n  object_id: ObjectId;\n  target_id: ObjectId;\n",
            name = action.name,
        ));
        // Optional fields based on action config
        if action.damage_type.is_some() {
            out.push_str("  damage_type?: DamageType;\n");
        }
        if action.cooldown.is_some() {
            out.push_str("  cooldown?: number;\n");
        }
        out.push_str("}\n\n");
    }

    out
}

fn ts_action_union(idl: &IdlDoc) -> String {
    let mut out = String::from("export type Action = ");

    let mut first = true;
    for cmd in &idl.core.commands {
        if cmd.name == "Action" || SPECIAL_COMMAND_ACTIONS.contains(&cmd.name.as_str()) {
            continue;
        }
        if !first {
            out.push_str("\n  | ");
        }
        first = false;
        out.push_str(&format!("{name}Action", name = cmd.name));
    }
    for name in SPECIAL_COMMAND_ACTIONS {
        if !first {
            out.push_str("\n  | ");
        }
        first = false;
        out.push_str(&format!("{name}Action"));
    }
    for action in &idl.mods.custom_actions {
        if SPECIAL_COMMAND_ACTIONS.contains(&action.name.as_str()) {
            continue;
        }
        if !first {
            out.push_str("\n  | ");
        }
        first = false;
        out.push_str(&format!("{name}Action", name = action.name));
    }
    out.push_str(";\n\n");

    out
}

fn ts_special_action_payload_type() -> &'static str {
    "export interface SpecialActionPayload {\n  resource?: ResourceName;\n  amount?: ResourceAmount;\n  range?: number;\n  structure?: StructureType;\n  damage_type?: DamageType;\n  cooldown?: number;\n}\n\n"
}

fn ts_command_factories(idl: &IdlDoc) -> String {
    let mut out = String::from(
        "export interface CommandIntent<A extends Action = Action> {\n  sequence: UInt32;\n  idempotency_key: string;\n  client_trace_id?: string;\n  action: A;\n}\n\nexport interface TickInput {\n  tick: UInt64;\n  player_id: PlayerId;\n  world_id: UInt64;\n  visible_snapshot: Uint8Array;\n  world_config_view: { config_hash: Uint8Array; payload: Uint8Array };\n  fuel_budget_hints: { fuel_remaining: UInt64; host_calls_remaining: UInt32; output_bytes_remaining: UInt32 };\n  message_inbox_cursor: { next_message_id: UInt64 };\n}\n\nexport interface TickResult<A extends Action = Action> {\n  commands: CommandIntent<A>[];\n  messages: PlayerMessage[];\n}\n\nexport interface PlayerMessage {\n  channel: \"Player\" | \"Debug\";\n  text: string;\n}\n\n",
    );
    out.push_str(ts_special_action_payload_type());
    // ---- addCost helper ----
    out.push_str(
        "export function addCost(target: ResourceCost, source: ResourceCost): ResourceCost {\n  for (const [res, amt] of Object.entries(source)) {\n    target[res] = (target[res] ?? 0) + amt;\n  }\n  return target;\n}\n\n",
    );
    // ---- command factory ----

    out.push_str("export function command<A extends Action>(sequence: number, idempotency_key: string, action: A, client_trace_id?: string): CommandIntent<A> {\n");
    out.push_str("  return client_trace_id === undefined ? { sequence, idempotency_key, action } : { sequence, idempotency_key, client_trace_id, action };\n");
    out.push_str("}\n\n");

    out.push_str("export const actions = Object.freeze({\n");

    // Core command factories
    for cmd in &idl.core.commands {
        if cmd.name == "Action" || SPECIAL_COMMAND_ACTIONS.contains(&cmd.name.as_str()) {
            continue;
        }
        let fn_name = to_camel_case(&cmd.name);
        let (required_fields, optional_fields): (Vec<_>, Vec<_>) =
            cmd.fields.iter().partition(|(_, t)| !t.ends_with('?'));
        let args: Vec<String> = required_fields
            .into_iter()
            .chain(optional_fields)
            .map(|(f, t)| {
                let ts_type = idl_to_ts_type(t);
                let optional = t.ends_with('?');
                if optional {
                    format!("{f}?: {ts_type}")
                } else {
                    format!("{f}: {ts_type}")
                }
            })
            .collect();

        // Spawn uses rename="Spawn" on wire
        let wire_type = if cmd.name == "Spawn" {
            "Spawn"
        } else {
            &cmd.name
        };

        out.push_str(&format!(
            "  {fn_name}: ({args}) => ({{ type: \"{wire_type}\"",
            args = args.join(", ")
        ));
        for (field, _) in &cmd.fields {
            out.push_str(&format!(", {field}"));
        }
        out.push_str(" }) as const,\n");
    }

    for name in SPECIAL_COMMAND_ACTIONS {
        let fn_name = to_camel_case(name);
        out.push_str(&format!(
            "  {fn_name}: (object_id: ObjectId, target_id: ObjectId, payload: SpecialActionPayload = {{}}) =>\n    ({{ type: \"{name}\", object_id, target_id, ...payload }}) as const,\n"
        ));
    }

    // Custom action factories (方案β: concrete typed factories)
    for action in &idl.mods.custom_actions {
        if SPECIAL_COMMAND_ACTIONS.contains(&action.name.as_str()) {
            continue;
        }
        let fn_name = to_camel_case(&action.name);
        let mut optional_args = Vec::new();
        let mut optional_fields = Vec::new();
        if action.damage_type.is_some() {
            optional_args.push("damage_type?: DamageType");
            optional_fields.push("damage_type");
        }
        if action.cooldown.is_some() {
            optional_args.push("cooldown?: number");
            optional_fields.push("cooldown");
        }
        let optional_args = if optional_args.is_empty() {
            String::new()
        } else {
            format!(", {}", optional_args.join(", "))
        };
        let optional_fields = if optional_fields.is_empty() {
            String::new()
        } else {
            format!(", {}", optional_fields.join(", "))
        };
        out.push_str(&format!(
            "  {fn_name}: (object_id: ObjectId, target_id: ObjectId{optional_args}) =>\n    ({{ type: \"{name}\", object_id, target_id{optional_fields} }}) as const,\n",
            fn_name = fn_name,
            name = action.name,
        ));
    }

    out.push_str("});\n\n");

    // body_energy_cost helper
    out.push_str(&format!(
        "export const BODY_PART_COST = {{\n{}\n}} as const;\n\n",
        idl.core
            .body_part_costs
            .iter()
            .map(|(k, v)| format!("  {k}: {{ Energy: {v} }}"))
            .collect::<Vec<_>>()
            .join(",\n")
    ));

    out.push_str(
        r#"export function bodyCost(body: BodyPart[]): ResourceCost {
  const total: ResourceCost = {};
  for (const part of body) {
    const cost = BODY_PART_COST[part] ?? {};
    for (const [res, amt] of Object.entries(cost)) {
      total[res] = (total[res] ?? 0) + amt;
    }
  }
  return total;
}
"#,
    );

    out
}

