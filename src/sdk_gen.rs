        assert!(!rust.contains("    Action {"));

        let schema = generate_command_intent_schema(&idl);
        assert_eq!(
            schema["definitions"]["CommandIntent"]["required"],
            serde_json::json!(["sequence", "idempotency_key", "action"])
        );
