use crate::world::WorldConfig;

/// CLI: dump IDL to stdout as JSON.
pub fn cli_dump_idl(world_toml_path: &str) -> Result<String, String> {
    let config = WorldConfig::from_world_toml(world_toml_path)
        .map_err(|e| format!("load world.toml: {e}"))?;
    let toml_bytes =
        std::fs::read(world_toml_path).map_err(|e| format!("read {world_toml_path}: {e}"))?;
    let world_hash = sha256_hex(&toml_bytes);

    let body_parts = crate::components::BodyPartRegistry::from_defs(config.body_part_types);
    let structures = crate::components::StructureTypeRegistry::default();
    let special_effects =
        crate::components::SpecialEffectRegistry::from_defs(config.special_effects);
    let custom_actions = crate::components::CustomActionRegistry::from_defs(config.custom_actions);

    let idl = crate::idl::extract_idl(
        &world_hash,
        &body_parts,
        &structures,
        &special_effects,
        &custom_actions,
    );

    serde_json::to_string_pretty(&idl).map_err(|e| format!("serialize IDL: {e}"))
}

/// CLI: generate SDK files to output directory.
pub fn cli_generate_sdk(world_toml_path: &str, out_base: &str) -> Result<(), String> {
    let idl_json = cli_dump_idl(world_toml_path)?;
    let idl: crate::idl::IdlDoc =
        serde_json::from_str(&idl_json).map_err(|e| format!("parse IDL: {e}"))?;

    let out_dir = std::path::PathBuf::from(out_base).join(&idl.world_hash);
    std::fs::create_dir_all(&out_dir).map_err(|e| format!("create {}: {e}", out_dir.display()))?;

    // Merge templates with generated code for TypeScript
    let ts_code = generate_typescript(&idl);
    let ts_sdk_dir = out_dir.join("sdk-ts");
    merge_sdk_templates("ts", &ts_sdk_dir)?;
    let ts_src = ts_sdk_dir.join("src");
    std::fs::create_dir_all(&ts_src).map_err(|e| format!("create {}: {e}", ts_src.display()))?;
    std::fs::write(ts_src.join("commands.ts"), ts_code)
        .map_err(|e| format!("write sdk-ts/commands.ts: {e}"))?;

    // Merge templates with generated code for Rust
    let rust_code = generate_rust(&idl);
    let rust_sdk_dir = out_dir.join("sdk-rust");
    merge_sdk_templates("rust", &rust_sdk_dir)?;
    let rust_src = rust_sdk_dir.join("src");
    std::fs::create_dir_all(&rust_src)
        .map_err(|e| format!("create {}: {e}", rust_src.display()))?;
    std::fs::write(rust_src.join("commands.rs"), rust_code)
        .map_err(|e| format!("write sdk-rust/commands.rs: {e}"))?;

    // Write IDL itself
    std::fs::write(out_dir.join("idl.json"), idl_json)
        .map_err(|e| format!("write idl.json: {e}"))?;

    // Write metadata
    let meta = serde_json::json!({
        "world_hash": idl.world_hash,
        "engine_version": idl.engine_version,
        "idl_version": idl.idl_version,
        "generated_at": chrono_now(),
    });
    std::fs::write(
        out_dir.join("meta.json"),
        serde_json::to_string_pretty(&meta).unwrap(),
    )
    .map_err(|e| format!("write meta.json: {e}"))?;

    Ok(())
}

fn chrono_now() -> String {
    // Simple UTC timestamp without chrono dependency
    use std::time::SystemTime;
    match SystemTime::now().duration_since(SystemTime::UNIX_EPOCH) {
        Ok(d) => format!("{}", d.as_secs()),
        Err(_) => "unknown".into(),
    }
}
