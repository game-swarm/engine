pub fn generate_command_intent_schema(_idl: &IdlDoc) -> serde_json::Value {
    serde_json::json!({
        "$schema": "https://json-schema.org/draft/2020-12/schema",
        "title": "Swarm tick() CommandIntent[]",
        "type": "array",
        "maxItems": 100,
        "items": { "$ref": "#/definitions/CommandIntent" },
        "definitions": {
            "CommandIntent": {
                "type": "object",
                "additionalProperties": false,
                "required": ["sequence", "idempotency_key", "action"],
                "properties": {
                    "sequence": { "type": "integer", "minimum": 0, "maximum": 4294967295_u64 },
                    "idempotency_key": { "type": "string", "minLength": 1 },
                    "client_trace_id": { "type": "string" },
                    "action": { "$ref": "#/definitions/Action" }
                }
            },
            "Action": {
                "oneOf": command_schema_branches()
                    .iter()
                    .map(command_schema_branch_schema)
                    .collect::<Vec<_>>()
            }
        }
    })
}

fn command_schema_branch_schema(branch: &CommandSchemaBranch) -> serde_json::Value {
    if branch.custom_wildcard {
        return serde_json::json!({
            "type": "object",
            "additionalProperties": true,
            "required": ["type", "object_id"],
            "propertyNames": {"not": {"enum": ["payload", "action_type", "action_name"]}},
            "properties": {
                "type": {"type": "string", "not": {"enum": reserved_command_action_names()}},
                "object_id": field_schema(&CommandSchemaField { name: "object_id", type_name: "ObjectId", required: true }),
                "target_id": field_schema(&CommandSchemaField { name: "target_id", type_name: "ObjectId", required: false }),
                "resource": field_schema(&CommandSchemaField { name: "resource", type_name: "ResourceName", required: false }),
                "amount": field_schema(&CommandSchemaField { name: "amount", type_name: "ResourceAmount", required: false }),
                "range": field_schema(&CommandSchemaField { name: "range", type_name: "u32", required: false }),
                "structure": field_schema(&CommandSchemaField { name: "structure", type_name: "StructureType", required: false }),
                "damage_type": field_schema(&CommandSchemaField { name: "damage_type", type_name: "DamageType", required: false }),
                "cooldown": field_schema(&CommandSchemaField { name: "cooldown", type_name: "u32", required: false })
            }
        });
    }
    let mut required = vec!["type".to_string()];
    required.extend(
        branch
            .fields
            .iter()
            .filter(|field| field.required)
            .map(|field| field.name.to_string()),
    );
    let mut properties = serde_json::Map::new();
    properties.insert(
        "type".to_string(),
        serde_json::json!({"const": branch.name}),
    );
    for field in &branch.fields {
        properties.insert(field.name.to_string(), field_schema(field));
    }
    serde_json::json!({
        "type": "object",
        "additionalProperties": false,
        "required": required,
        "properties": properties,
    })
}

fn reserved_command_action_names() -> Vec<&'static str> {
    command_schema_branches()
        .into_iter()
        .filter(|branch| !branch.custom_wildcard)
        .map(|branch| branch.name)
        .collect()
}

fn field_schema(field: &CommandSchemaField) -> serde_json::Value {
    match field.type_name {
        "ObjectId" => serde_json::json!({"type": "integer", "minimum": 0}),
        "PlayerId" | "ResourceAmount" | "u32" => {
            serde_json::json!({"type": "integer", "minimum": 0, "maximum": 4294967295_u64})
        }
        "Direction" => {
            serde_json::json!({"type": "string", "enum": ["North", "South", "East", "West"]})
        }
        "ResourceName" | "StructureType" | "DamageType" => serde_json::json!({"type": "string"}),
        "u64" => serde_json::json!({"type": "integer", "minimum": 0}),
        "i32" => serde_json::json!({"type": "integer"}),
        "BodyPart[]" => {
            serde_json::json!({"type": "array", "items": {"type": "string", "enum": ["Move", "Work", "Carry", "Attack", "RangedAttack", "Heal", "Claim", "Tough"]}, "minItems": 1, "maxItems": crate::command::MAX_BODY_PARTS})
        }
        "JsonValue" => serde_json::json!(true),
        _ => serde_json::json!({"type": "string"}),
    }
}

fn ts_constants(idl: &IdlDoc) -> String {
    let mut out = String::from("\n// ── Constants ──\n\n");
    out.push_str(&format!(
        "export const SDK_VERSION = \"{}\";\n",
        idl.engine_version
    ));
    out.push_str(&format!(
        "export const IDL_VERSION = \"{}\";\n",
        idl.idl_version
    ));
    out.push_str(&format!("export const ABI_VERSION = {ABI_VERSION};\n\n"));
    // AI game data markers (used by tick protocol)
    out.push_str("export const AI_GAME_DATA_START = \"___AI_GAME_DATA_START___\";\n");
    out.push_str("export const AI_GAME_DATA_END = \"___AI_GAME_DATA_END___\";\n\n");

    for (name, value) in &idl.core.constants {
        let js_val = match value {
            serde_json::Value::Number(n) => integer_constant(name, n).literal(),
            serde_json::Value::Bool(b) => b.to_string(),
            _ => format!("{value}"),
        };
        out.push_str(&format!("export const {name} = {js_val};\n"));
    }

    out
}

fn to_camel_case(s: &str) -> String {
    let mut result = String::new();
    for (i, c) in s.chars().enumerate() {
        if i == 0 {
            result.push(c.to_lowercase().next().unwrap_or(c));
        } else {
            result.push(c);
        }
    }
    result
}

// ── Rust generation ─────────────────────────────────────────────────

pub fn generate_rust(idl: &IdlDoc) -> String {
    let mut out = String::new();

    out.push_str(&rust_header(idl));
    out.push('\n');
    // Type aliases live in types_template.rs; only enums + commands generated here.
    out.push_str(&rust_enums(idl));
    out.push('\n');
    out.push_str(&rust_structure_types(idl));
    out.push('\n');
    out.push_str(&rust_command_action(idl));
    out.push('\n');
    out.push_str(&rust_command_struct());
    out.push('\n');
    out.push_str(&rust_custom_constructors(&idl.mods.custom_actions));
    out.push('\n');
    out.push_str(&rust_constants(idl));
    out
}

fn rust_header(idl: &IdlDoc) -> String {
    format!(
        "// @generated by swarm-engine idl v{idl_version}\n\
         // world hash: {world_hash}\n\
         // engine version: {engine_version}\n\
         // DO NOT EDIT — this file is auto-generated.\n\n\
         use serde::{{Deserialize, Serialize}};\n",
        idl_version = idl.idl_version,
        world_hash = idl.world_hash,
        engine_version = idl.engine_version,
    )
}

fn rust_enums(idl: &IdlDoc) -> String {
    let mut out = String::new();

    // Direction
    out.push_str("#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]\n");
    out.push_str("pub enum Direction {\n");
    for v in &idl.core.enums.direction {
        out.push_str(&format!("    {v},\n"));
    }
    out.push_str("}\n\n");
    out.push_str(
        "impl Direction {\n    pub const fn offset(self) -> (i32, i32) {\n        match self {\n",
    );
    for v in &idl.core.enums.direction {
        let (dx, dy) = direction_offset(v);
        out.push_str(&format!("            Direction::{v} => ({dx}, {dy}),\n"));
    }
    out.push_str("        }\n    }\n}\n\n");

    // BodyPart
    out.push_str("#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]\n");
    out.push_str("pub enum BodyPart {\n");
    for v in &idl.core.enums.body_part {
        out.push_str(&format!("    {v},\n"));
    }
    out.push_str("}\n\n");

    // DamageType
    out.push_str("#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]\n");
    out.push_str("pub enum DamageType {\n");
    for v in &idl.core.enums.damage_type {
        out.push_str(&format!("    {v},\n"));
    }
    out.push_str("}\n");

    out
}

fn direction_offset(v: &str) -> (i32, i32) {
    match v {
        "Top" => (0, -1),
        "TopRight" => (1, -1),
        "BottomRight" => (1, 0),
        "Bottom" => (0, 1),
        "BottomLeft" => (-1, 1),
        "TopLeft" => (-1, 0),
        _ => (0, 0),
    }
}

fn rust_structure_types(idl: &IdlDoc) -> String {
    let mut out = String::new();
    out.push_str("#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, PartialOrd, Ord)]\n");
    out.push_str("pub struct StructureType(pub &'static str);\n\n");
    out.push_str("impl StructureType {\n");

    let all: Vec<&str> = idl
        .core
        .structure_types
        .iter()
        .chain(idl.mods.structure_types.iter().map(|s| &s.name))
        .map(|s| s.as_str())
        .collect();

    for s in &all {
        let const_name = s.to_uppercase().replace('-', "_");
        out.push_str(&format!(
            "    pub const {const_name}: Self = Self(\"{s}\");\n"
        ));
    }

    out.push_str("\n    pub fn new(name: impl Into<String>) -> Self {\n");
    out.push_str("        Self(Box::leak(name.into().into_boxed_str()))\n");
    out.push_str("    }\n\n");
    out.push_str("    pub fn as_str(self) -> &'static str {\n        self.0\n    }\n");

    // Add PascalCase aliases (backward compat)
    for s in &all {
        if s.chars().next().map(|c| c.is_uppercase()).unwrap_or(false) {
            out.push_str(&format!(
                "    #[allow(non_upper_case_globals)]\n    pub const {s}: Self = Self::{};\n",
                s.to_uppercase().replace('-', "_")
            ));
        }
    }
    out.push_str("}\n\n");

    // Custom Serialize / Deserialize (same pattern as engine's StructureType)
    out.push_str("impl Serialize for StructureType {\n");
    out.push_str(
        "    fn serialize<S: serde::Serializer>(&self, s: S) -> Result<S::Ok, S::Error> {\n",
    );
    out.push_str("        s.serialize_str(self.0)\n    }\n}\n\n");
    out.push_str("impl<'de> Deserialize<'de> for StructureType {\n");
    out.push_str(
        "    fn deserialize<D: serde::Deserializer<'de>>(d: D) -> Result<Self, D::Error> {\n",
    );
    out.push_str("        String::deserialize(d).map(Self::new)\n    }\n}\n\n");
    out.push_str("impl std::fmt::Display for StructureType {\n");
    out.push_str("    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {\n");
    out.push_str("        f.write_str(self.0)\n    }\n}\n\n");
    out.push_str("impl Default for StructureType {\n");
    out.push_str("    fn default() -> Self {\n        Self::SPAWN\n    }\n}\n");

    out
}

fn rust_command_action(idl: &IdlDoc) -> String {
    let mut out = String::new();

    out.push_str("#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]\n");
    out.push_str("#[serde(tag = \"type\", deny_unknown_fields)]\n");
    out.push_str("pub enum CommandAction {\n");

    for cmd in &idl.core.commands {
        if cmd.name == "Action" || SPECIAL_COMMAND_ACTIONS.contains(&cmd.name.as_str()) {
            continue;
        }
        let variant_name = &cmd.name;
        if cmd.fields.is_empty() {
            out.push_str(&format!("    {variant_name},\n"));
        } else {
            out.push_str(&format!("    {variant_name} {{\n"));
            for (field, ftype) in &cmd.fields {
                let rust_type = idl_to_rust_type(ftype);
                out.push_str(&format!("        {field}: {rust_type},\n"));
            }
            out.push_str("    },\n");
        }
    }

    for name in SPECIAL_COMMAND_ACTIONS {
        out.push_str(&format!(
            "    {name} {{\n{fields}    }},\n",
            fields = rust_special_action_fields(),
        ));
    }

    for action in &idl.mods.custom_actions {
        if SPECIAL_COMMAND_ACTIONS.contains(&action.name.as_str()) {
            continue;
        }
        out.push_str(&format!(
            "    {name} {{\n        object_id: u64,\n        target_id: u64,\n",
            name = action.name
        ));
        if action.damage_type.is_some() {
            out.push_str("        damage_type: Option<DamageType>,\n");
        }
        if action.cooldown.is_some() {
            out.push_str("        cooldown: Option<u32>,\n");
        }
        out.push_str("    },\n");
    }

    out.push_str("}\n");
    out
}

fn rust_special_action_fields() -> &'static str {
    "        object_id: u64,\n        target_id: u64,\n        resource: Option<String>,\n        amount: Option<u32>,\n        range: Option<u32>,\n        structure: Option<StructureType>,\n        damage_type: Option<DamageType>,\n        cooldown: Option<u32>,\n"
}

